package main

import (
	"fmt"
	"os"
	"os/exec"
	"path/filepath"
	"runtime"
	"strings"

	cdx "github.com/CycloneDX/cyclonedx-go"
)

func generateSBOM(projectPath, projType string, verbose bool) (bom *cdx.BOM, err error) {
	var cachedBinaryPath string
	defer func() {
		if err == nil {
			return
		}

		if cachedBinaryPath != "" && fileExists(cachedBinaryPath) {
			if err := os.Remove(cachedBinaryPath); err != nil && verbose {
				fmt.Printf("‚ö†Ô∏è Warning: Could not delete temporary file %s: %v\n", cachedBinaryPath, err)
			}
		}
	}()

	// Convert to absolute path
	absolutePath, err := filepath.Abs(projectPath)
	if err != nil {
		return nil, fmt.Errorf("failed to get absolute path: %w", err)
	}

	// Check if path exists
	if !fileExists(absolutePath) && !dirExists(absolutePath) {
		return nil, fmt.Errorf("path does not exist: %s", absolutePath)
	}

	fmt.Printf("üîç Generating SBOM for: %s\n", absolutePath)

	// Extract embedded binary to cache (or use cached version)
	cachedBinaryPath, err = extractEmbeddedBinary(verbose)
	if err != nil {
		return nil, fmt.Errorf("failed to extract embedded binary: %w", err)
	}

	// Create temporary file for cdxgen output
	tempBOMFile, err := os.CreateTemp("", "rudor-bom-*.json")
	if err != nil {
		return nil, fmt.Errorf("failed to create temporary BOM file: %w", err)
	}
	tempBOMPath := tempBOMFile.Name()
	tempBOMFile.Close()

	// Ensure cleanup of temporary BOM file
	defer func() {
		if err := os.Remove(tempBOMPath); err != nil && verbose {
			fmt.Printf("‚ö†Ô∏è Warning: Could not delete temporary BOM file %s: %v\n", tempBOMPath, err)
		}
	}()

	// Build cdxgen arguments using the temporary file
	args := []string{"-o", tempBOMPath}

	// Add project type if specified
	if projType != "" {
		args = append(args, "-t", projType)
	}

	// Add the path to scan at the end
	args = append(args, absolutePath)

	if verbose {
		fmt.Printf("Running: %s %s\n", cachedBinaryPath, strings.Join(args, " "))
	}

	if verbose {
		fmt.Printf("üîß Using temporary BOM file: %s\n", tempBOMPath)
	}

	// Execute cdxgen
	cmd := exec.Command(cachedBinaryPath, args...)

	if verbose {
		cmd.Stdout = os.Stdout
		cmd.Stderr = os.Stderr
	}

	if err := cmd.Run(); err != nil {
		if exitErr, ok := err.(*exec.ExitError); ok {
			return nil, fmt.Errorf("cdxgen failed with exit code: %d", exitErr.ExitCode())
		}
		return nil, fmt.Errorf("failed to execute cdxgen: %w", err)
	}

	// Check if temporary BOM file was created
	if !fileExists(tempBOMPath) {
		return nil, fmt.Errorf("SBOM file was not generated by cdxgen")
	}

	// Enhance the BOM with Rudor metadata and return it
	bom, err = enhanceBOM(tempBOMPath, verbose)
	if err != nil {
		return nil, fmt.Errorf("failed to enhance BOM: %w", err)
	}

	fmt.Printf("‚úÖ SBOM generated successfully\n")
	return bom, nil
}

func extractEmbeddedBinary(verbose bool) (string, error) {
	binaryData, binaryName, err := getEmbeddedBinary()
	if err != nil {
		return "", err
	}

	// Build versioned cache filename (e.g., cdxgen-1.0.0.exe)
	extension := filepath.Ext(binaryName)
	cachedFileName := fmt.Sprintf("cdxgen-%s%s", version, extension)
	cachedPath := filepath.Join(os.TempDir(), cachedFileName)

	// Check if cached binary already exists
	if fileExists(cachedPath) {
		if verbose {
			fmt.Printf("‚úÖ Using cached binary: %s\n", cachedPath)
		}
		return cachedPath, nil
	}

	if verbose {
		fmt.Printf("üîß Extracting embedded binary: %s\n", binaryName)
	}

	// Write embedded binary to cache
	if err := os.WriteFile(cachedPath, binaryData, 0755); err != nil {
		return "", fmt.Errorf("failed to write binary to cache: %w", err)
	}

	// Make executable on Unix-like systems
	if runtime.GOOS != "windows" {
		if err := os.Chmod(cachedPath, 0755); err != nil {
			return "", fmt.Errorf("failed to make binary executable: %w", err)
		}
	}

	if verbose {
		fmt.Printf("‚úÖ Binary cached to: %s\n", cachedPath)
	}

	return cachedPath, nil
}

